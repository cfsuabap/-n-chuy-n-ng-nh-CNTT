<li><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;"><strong>Chúng tôi luôn tạo nên môi trường học tập thoải mái, thân thiện</strong> với sự hướng dẫn tận tình từ tất cả các phòng ban của nhà trường<strong>.</strong> Nếu có bất cứ thắc mắc nào, bạn sẽ được hỗ trợ giải đáp ngay lập tức.</span></li>
