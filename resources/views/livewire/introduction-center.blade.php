<h2 style="text-align: center;"><span style="font-size: 18pt;"><strong><span style="font-family: arial, helvetica, sans-serif; background-color: #ff6600; color: #ffffff;">CÁC KHÓA HỌC ĐANG ĐÀO TẠO</span></strong></span></h2>
<p><strong><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt; color: #ff0000;">1. Khóa Học A1</span></strong></p>
<p><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Khóa học dành cho những học viên có nhu cầu <strong>học bằng lái xe máy</strong>, với lịch thi sớm. Học viên có lịch sát hạch sau khoảng 1 tháng đến 1 tháng rưỡi. Được giáo viên hướng dẫn đầy đủ từ lý thuyết đến ôn luyện sa hình.</span></p>
<p><strong><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt; color: #ff0000;">2. Khóa Học B1</span></strong></p>
<p><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Khóa học dành cho những học viên thi bằng lái xe ô tô dưới 9 chỗ ngồi số tự động, bằng B1 không có giá trị kinh doanh, chỉ lái xe gia đình. Hiện nay bằng B1 đang được rất nhiều học viên nữ ưa chuộng vì <strong>học và thi đều khá dễ</strong>. Lịch thi thường là sau khoảng 4 đến 4,5 tháng kể từ ngày đăng ký.</span></p>
<p><strong><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt; color: #ff0000;">3. Khóa Học B2</span></strong></p>
<p><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Khóa học dành cho những học viên muốn lái cả hai loại xe ô tô dưới 9 chỗ ngồi là <strong>tự động và số sàn</strong>, ngoài ra bằng cũng có thể sử dụng kinh doanh như chạy: Grab, taxi, xe dịch vụ,… Bạn sẽ có lịch thi sau khoảng 3 đến 3,5 tháng kể từ ngày đăng ký.</span></p>
<p><span style="color: #ff0000;"><strong><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">4. Khóa Học C</span></strong></span></p>
<p><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt;">Khóa học dành cho những học viên <strong>có nhu cầu lái xe tải</strong>, xe chở hàng nặng trên 3,5 tấn. Lịch thi của khóa bằng C thường là 6 tháng kể từ ngày đăng ký</span></p>
<p><strong><span style="font-family: arial, helvetica, sans-serif; font-size: 12pt; color: #ff0000;">5. Khóa nâng dấu</span></strong></p>
